# Cria uma lista com vários nomes
nomes = ["Andre", "Bruna", "Cleiton", "Fausto", "Turu"]

# Mostra a lista inteira
print("Lista de nomes:", nomes)

# Pula uma linha (\n) e mostra um título
print("\nImprimindo cada nome:")

# Loop que percorre cada item da lista
for nome in nomes:
    # A cada repetição, imprime um nome da lista
    print(nome)
